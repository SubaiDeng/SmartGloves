#include "delay.h"

/**************************************************************
*        Global Value Define Section
**************************************************************/
u32 TimeDelay;//计时变量

/**************************************************************
*	Function Define Section
**************************************************************/
/**
  * @说明     延时函数
  * @参数     nTime: 延时时间
  * @返回值   None
  */
void DelayMs(uint32_t nTime)
{
    TimeDelay = nTime;
    while(TimeDelay != 0);
}
